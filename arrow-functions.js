
// Arrow Functions (()=>{})

// Arrow function to add two numbers
const add = (a, b) => a + b;

// Arrow function to square a number
const square = n => n * n;

// Arrow function with no parameters
const greet = () => "Hello ,World";

console.log("Add: ", add(5, 3));       
console.log("Square: ", square(4));    
console.log("Greet: ", greet()); 